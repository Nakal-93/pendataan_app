function testConnection($config) {
    try {
        $dsn = "mysql:host={$config['host']}";
        if (!empty($config['port']) && !strpos($config['host'], ':')) {
            $dsn .= ";port={$config['port']}";
        }
        $dsn .= ";charset=utf8mb4";
        
        $pdo = new PDO($dsn, $config['user'], $config['pass'], [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
            PDO::ATTR_TIMEOUT => 5
        ]);
        
        // Get MySQL version
        $stmt = $pdo->query("SELECT VERSION() as version");
        $version = $stmt->fetch(PDO::FETCH_ASSOC)['version'];
        
        // Try to create database
        try {
            $pdo->exec("CREATE DATABASE IF NOT EXISTS aplikasi_madiun CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
            $dbCreated = true;
        } catch (Exception $e) {
            $dbCreated = false;
        }
        
        return [
            'success' => true,
            'message' => "‚úÖ Connection SUCCESS!<br>MySQL Version: {$version}" . ($dbCreated ? "<br>Database 'aplikasi_madiun' ready!" : ""),
            'config' => $config
        ];
        
    } catch (PDOException $e) {
        return [
            'success' => false,
            'message' => "‚ùå Connection FAILED: " . $e->getMessage(),
            'config' => $config
        ];
    }
}

